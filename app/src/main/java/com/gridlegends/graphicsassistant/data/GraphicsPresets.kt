    private fun preset(
        name: String,
        description: String,
        fps: String,
        height: String,
        minPercent: String,
        maxPercent: String,
        dynamic: String,
        texture: String,
        aa: String,
        shadows: String,
        lod: String,
        ground: String,
        crowds: String,
        post: String,
        ssr: String,
        volumetrics: String,
        hdr: String,
        graphicsLevel: String,
        graphicsShadows: String,
        graphicsDynamic: String,
        graphicsOptimisation: String
    ): GraphicsPreset {
        val values = mapOf(
            "FramerateLimit" to fps,
            "DynamicResolution" to dynamic,
            "TextureQuality" to texture,
            "AntiAliasing" to aa,
            "Shadows" to shadows,
            "LODQuality" to lod,
            "GroundCover" to ground,
            "Crowds" to crowds,
            "PostProcess" to post,
            "MotionBlur" to if (post == "2") "3" else "1",
            "ScreenSpaceReflections" to if (ssr == "3") "2" else "1",
            "VehicleReflections" to if (graphicsLevel == "3, 0, 0" && post == "2") "3" else "1",
            "SmokeShadows" to if (post == "2") "2" else "1",
            "VolumetricLights" to if (volumetrics == "3") "2" else "1",
            "FireworksAndConfetti" to if (post == "2") "3" else "1",
            "HDR" to hdr,
            "PresetOverride" to if (post == "2") "1" else "0",
            "DevicePreset" to if (post == "2") "1" else "0",
            "Setup/FixedScreenHeight" to height,
            "Setup/EnableSSR" to ssr,
            "Setup/EnableVolumetrics" to volumetrics,
            "Setup/EnableEdgeBlurOnQuarterRes" to if (height == "360") "0" else "1",
            "Graphics/FramerateLimit" to fps,
            "Graphics/FrameSizeMinimumPercentage" to minPercent,
            "Graphics/FrameSizeMaximumPercentage" to maxPercent,
            "Graphics/OptimisationResolutionMultiplier" to graphicsOptimisation,
            "Graphics/DynamicResolution" to graphicsDynamic,
            "Graphics/TextureQuality" to graphicsLevel,
            "Graphics/AntiAliasing" to graphicsLevel,
            "Graphics/Shadows" to graphicsShadows,
            "Graphics/LODQuality" to graphicsLevel,
            "Graphics/GroundCover" to graphicsLevel,
            "Graphics/Crowds" to when (crowds) {
                "0" -> "0, 0, 0"
                "1" -> if (graphicsLevel == "3, 0, 0") "2, 0, 0" else "1, 0, 0"
                else -> "1, 0, 0"
            },
            "Graphics/PostProcess" to if (post == "2") "3, 0, 0" else graphicsLevel,
            "Graphics/ScreenSpaceReflections" to if (ssr == "3") "3, 0, 0" else graphicsLevel,
            "Graphics/VehicleReflections" to graphicsLevel,
            "Graphics/VolumetricLights" to if (volumetrics == "3") "3, 0, 0" else "1, 0, 0",
            "Graphics/WeatherEffects" to graphicsLevel,
            "Graphics/Particles" to if (post == "2") "3, 0, 0" else "0, 0, 0",
            "Graphics/MetalFX" to if (graphicsLevel == "3, 0, 0") "3, 0, 0" else "0, 0, 0",
            "Graphics/Mirrors" to if (graphicsLevel == "3, 0, 0") "3, 0, 0" else "0, 0, 0",
            "Graphics/LightingQuality" to graphicsLevel,
            "Graphics/SmokeShadows" to if (post == "2") "3, 0, 0" else "1, 0, 0",
            "Graphics/Skidmarks" to graphicsLevel,
            "Graphics/AmbientOcclusion" to graphicsLevel,
            "Performance/FramerateLimit" to if (fps == "60") "60" else "120",
            "Performance/FrameSizeMaximumPercentage" to if (fps == "60") "90" else "80",
            "Performance/OptimisationResolutionMultiplier" to if (fps == "60") "86" else "100",
            "Performance/LODQuality" to if (fps == "60") "1, 0, 0" else "2, 0, 0",
            "Performance/VehicleReflections" to if (fps == "60") "1, 0, 0" else "3, 0, 0",
            "Performance/CubeMapUpdateModeInterior" to if (fps == "60") {
                "oddsThenEvens, None, None"
            } else {
                "None, None, None"
            }
        )
        return GraphicsPreset(name, description, values)
    }
